// Готовые нарративные шаблоны для каждого сценария.
// Функция получает survivors (массив игроков с traits) и exiled (массив изгнанных).
// Возвращает строку с подставленными данными.

function names(players) {
  return players.map(p => p.name).join(", ") || "никого";
}

function trait(player, key) {
  const t = player?.traits?.[key];
  return t?.value || "неизвестно";
}

function professions(players) {
  return players.map(p => `${p.name} (${trait(p, "profession")})`).join(", ");
}

function healthIssues(players) {
  return players
    .filter(p => {
      const h = trait(p, "health").toLowerCase();
      return h !== "полностью здоров" && h !== "абсолютно здоров (атлет)" && h !== "идеальный иммунитет";
    })
    .map(p => `${p.name} — ${trait(p, "health")}`)
    .join("; ");
}

function usefulSkills(players) {
  return players.map(p => {
    const f1 = trait(p, "fact1");
    return `${p.name}: ${f1}`;
  }).join("; ");
}

